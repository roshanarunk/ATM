    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
      //returns to menu after doing an action// 
        if (whatisbeingInput.equals("return")){
            menuButtons_mode();
        }
        
        
        if (whatisbeingInput.equals("PINCHANGE")){
            if (inputNumbers.length()== 6){
                Roshan.getAccount("0000000001").setPinNumber(inputNumbers);
                 MessageText.setText("Your pin has been changed");
                hideInputNumbers = false;
                whatisbeingInput = "return";
            }
            else {
                MessageText.setText("Pin is too short or too long please try again ");
                whatisbeingInput = "return";
            }
           
        }
        
        //Checks the pin//
        if (whatisbeingInput.equals("PIN") || whatisbeingInput.equals("CHANGEPIN")) {
            if (Roshan.getAccount("0000000001").checkPin(inputNumbers)){
                Pin_attempts = 0;
                if (whatisbeingInput.equals("CHANGEPIN")){
                    TextLabel.setText("");
                    inputNumbers = "";
                    MessageText.setText("Please enter your new pin");
                    whatisbeingInput = "PINCHANGE";
                }
                else{
                    hideInputNumbers = false;
                    menuButtons_mode();
                }
                
                
            }
            else {
                Pin_attempts += 1;
                TextLabel.setText("");
                inputNumbers = "";
                MessageText.setText("Wrong pin please try again");
                if (Pin_attempts >= 3){
                    setVisible(false);
                    dispose();
                }
            }
        }
        //withdraw money//
        if (whatisbeingInput.equals("withdraw")){
            Double x = Double.parseDouble(inputNumbers);
           if (Roshan.getAccount("0000000001").getBalance()>= x) {
            Roshan.getAccount("0000000001").addTransaction(new Transactions(new Date(),Transactions.TRANS_TYPE_WITHDRAWAL,x));
            MessageText.setText("New Balance is");
            inputNumbers = Double.toString(Roshan.getAccount("0000000001").getBalance());
            TextLabel.setText(inputNumbers);
            whatisbeingInput = "return";
           }
           else {
                numberButtons_mode();
                MessageText.setText("Insufficient Funds");
                whatisbeingInput="return";
            }
           
        }
        
        if (whatisbeingInput.equals("deposit")) {
            Double y =Double.parseDouble(inputNumbers);
            Roshan.getAccount("0000000001").addTransaction(new Transactions(new Date(),Transactions.TRANS_TYPE_DEPOSIT,y));
            MessageText.setText("New Balance is");
            inputNumbers = Double.toString(Roshan.getAccount("0000000001").getBalance());
            TextLabel.setText(inputNumbers);
            whatisbeingInput = "return";   
            
          
            
           
        }
            
    }//GEN-LAST:event_btnEnterActionPerformed

