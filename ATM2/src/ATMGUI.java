     private void addNumber(String num){

        
        if (numberButtonsEnabled){
            inputNumbers=inputNumbers.concat(num);
            if (!hideInputNumbers){
                
               TextLabel.setText(inputNumbers);
            }
            else{
                StringBuffer stars = new StringBuffer(inputNumbers.length());
                for (int i=0;i<inputNumbers.length();i++){
                    stars.append("*");
                }
                TextLabel.setText(stars.toString());
            }
        }
    }
            
            
